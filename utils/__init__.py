# This is an __init__.py file.
# It can be empty, but its presence in a directory tells Python that the directory
# should be treated as a "package".
#
# A package is a way of organizing related modules (Python files) together.
# Because this file exists, we can now write `from utils.helpers import ...`
# from other parts of our project.
#
# Without this file, Python wouldn't know to look inside the `utils` directory
# when we try to import from it.
